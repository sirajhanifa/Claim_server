const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
        trim: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        required: true,
        enum: ['admin', 'staff', 'finance'],
        default: 'staff'
    }
}, { timestamps: true });

module.exports = mongoose.model('user', userSchema);